var dia
dia= parseInt(prompt("Digite um número correspondente ao dia da semana"))
switch(dia){
    
    case 1:
        {
            alert("Domingo")
            break
        }
    
    case 2:
        {
            alert("segunda-feira")
            break
        }
        case 3:{
            alert("terça-feira")
            break
        }
        case 4:{
            alert("quarta-feira")
            break
        }
        case 5:
            {
                alert("quinta-feira")
                break
            }
            case 6:
        {
            alert("sexta-feira")
            break
        }
        case 7:
        {
            alert("sabádo")
            break
        }
        default:{
            alert("operação valida")
        } 






}         